public class TesteCaracteres {
	
	public static void main (String[] args) {
		
		char letraA = 'a';
		char letraB = 'b';
		
		System.out.println(letraA);
		System.out.println(letraB);
		
		char valor = 66;
		System.out.println(valor);

		valor = (char) (valor+1);
		System.out.println(valor);
		
		String palavra = "Alura cursos online de tenologia";
		System.out.println(palavra);
		
		palavra = palavra + 2020;
		System.out.println(palavra);
		
		String parcela1 = "10";
		String parcela2 = "20";

		System.out.println(parcela1 + parcela2);
	}

}
