
public class TiposVariaveis {
	
	public static void main(String[] args) {
		
		int n1 = 10;
		double n2 = 3.1415;
		char letra = 'A';
		String nome = "Régis";
		
		System.out.println("Número Inteiro: " + n1);
		System.out.println("Número Real: " + n2);
		System.out.println("Letra: " + letra);
		System.out.println("Nome: " + nome);
		
	}

}
