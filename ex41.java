package lista_execícios;

public class ex41 {
	public static void main(String[] args) {
		
		int n = 100;
		
		while (n >= 0) {
			System.out.print(n + " ");
			n-=5;
		}
		
		System.out.println();
		System.out.println("Acabou!");
		
	}

}

